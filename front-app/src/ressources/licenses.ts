const getPackageInfo = async (packageName: string) => {
	const response = await fetch(`https://registry.npmjs.org/${packageName}`, { cache: "no-store" });
	const data = await response.json();
	return {
		name: data.name,
		url: data.homepage || (data.repository ? data.repository.url : "N/A"),
		author: data.author ? data.author.name : "Unknown",
		license: data.license,
	};
};

const getAllDependenciesInfo = async () => {
	const promises = dependenciesName.map((dep) => getPackageInfo(dep));
	const results = await Promise.all(promises);
	return results;
};

